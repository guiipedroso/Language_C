#include <stdio.h>

int main()
  {
  float pi = 3.1415,
	r = 6.0, h = 7.3, 
	volume;
  volume = pi*r*r*h;
  printf("Cylinder volume = %.2f\n", volume);  
  getchar(); 
  return(0);
  } /* end main */

